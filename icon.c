void showIcon(const char icon[], int width, unsigned char pageNum, unsigned char page, unsigned char col) // pageNum: how many pages the icon uses.
{
	int startIndex = 0;
	for (int p = 0; p < pageNum; p++)
	{
		for (int i = col; i < col+width; i++)
		{
			I2CDataBuffer[page+p][i] = icon[startIndex++];
		}
	}
}

void hideIcon(int width, unsigned char page, unsigned char col)
{
	int startIndex = 0;
	// upper page:
	for (int i = col; i < col+width; i++)
	{
		I2CDataBuffer[page][i] = 0;
	}
	// lower page:
	for (int i = col; i < col+width; i++)
	{
		I2CDataBuffer[page+1][i] = 0;
	}
}
