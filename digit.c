/*
** ===================================================================
**     Method      :  		showDigit
**     Description :		Show a single digit start at position
**     						specified by page and column.
**     Parameters  :
**         NAME            - DESCRIPTION
**         font[]          - font table for the digit
**         digit		   - choose from digit 0 --> digit 9
**         heightInPages   - height of digit in pages
**         widthInRows     - width of digit in rows
**         page			   - start position: page number
**         column 		   - start position: column number
** ===================================================================
*/
void showDigit(const char font[], unsigned char digit, unsigned char heightInPages, unsigned char widthInRows, unsigned char page, unsigned char column)
{
	int startIndex = digit*(heightInPages*widthInRows);
	int width = widthInRows;
	int pageNumber;
	for (pageNumber = page; pageNumber < page+heightInPages; pageNumber++)
	{
		for (int i = column; i < column+width; i++)
		{
			I2CDataBuffer[pageNumber][i] = font[startIndex++];
		}
	}
}

/*
** ===================================================================
**     Method      :  		clearDigit
**     Description :		Clear a single digit start at position
**     						specified by page and column.
**     Parameters  :
**         NAME            - DESCRIPTION
**         heightInPages   - height of digit in pages
**         widthInRows     - width of digit in rows
**         page			   - start position: page number
**         column 		   - start position: column number
** ===================================================================
*/
void clearDigit(unsigned char widthInRows, unsigned char heightInPages, unsigned char page, unsigned char column)
{
	int pageNumber;
	for (pageNumber = page; pageNumber < page+heightInPages; pageNumber++)
	{
		for (int i = column; i < column+widthInRows; i++) {
			I2CDataBuffer[pageNumber][i] = 0;
		}
	}
}

/*
** ===================================================================
**     Method      :  		showIntegerRightAligned
**     Description :		Show a integer start at position specified
**     						by page and column up to three digits. The
**     						rightmost position should be the start
**     						position.
**     Parameters  :
**         NAME            - DESCRIPTION
**         font[]          - font table for the digit.
**         number		   - number to be displayed (-999 to 999)
**         heightInPages   - height of digit in pages
**         widthInRows     - width of digit in rows
**         page			   - start position: page number
**         column 		   - start position: column number
** ===================================================================
*/
void showIntegerRightAligned(const char font[], int number, unsigned char heightInPages, unsigned char widthInRows, unsigned char page, unsigned char column)
{
	unsigned char hundreds = number/100;
	unsigned char tens = (number%100)/10;
	unsigned char ones = number%10;

	if (number >= 100 && number <= 999)
	{
		clearArea(page+heightInPages/2, column-widthInRows*3-NEGATIVE_SIGN_WIDTH, page+heightInPages/2, column-widthInRows*3);
		showDigit(font, hundreds, heightInPages, widthInRows, page, column-widthInRows*3);
		showDigit(font, tens, heightInPages, widthInRows, page, column-widthInRows*2);
		showDigit(font, ones, heightInPages, widthInRows, page, column-widthInRows);
	}
	else if (number < 100 && number >= 10)
	{
		clearDigit(widthInRows, heightInPages, page, column-widthInRows*3);
		clearArea(page+heightInPages/2, column-widthInRows*3-NEGATIVE_SIGN_WIDTH, page+heightInPages/2, column-widthInRows*3);

		showDigit(font, tens, heightInPages, widthInRows, page, column-widthInRows*2);
		showDigit(font, ones, heightInPages, widthInRows, page, column-widthInRows);
	}
	else if (number < 10 && number >= 0)
	{
		clearDigit(widthInRows, heightInPages, page, column-widthInRows*3);
		clearDigit(widthInRows, heightInPages, page, column-widthInRows*2);
		clearArea(page+heightInPages/2, column-widthInRows*3-NEGATIVE_SIGN_WIDTH, page+heightInPages/2, column-widthInRows*3);

		showDigit(font, ones, heightInPages, widthInRows, page, column-widthInRows);
	}
	else if (number < 0 && number > -10)
	{
		clearDigit(widthInRows, heightInPages, page, column-widthInRows*3);
		clearDigit(widthInRows, heightInPages, page, column-widthInRows*2);
		clearArea(page+heightInPages/2, column-widthInRows*3-NEGATIVE_SIGN_WIDTH, page+heightInPages/2, column-widthInRows*3);

		showIcon(negativeSign, NEGATIVE_SIGN_WIDTH, 1, page+heightInPages/2, column-widthInRows-NEGATIVE_SIGN_WIDTH);
		ones = (-number)%10;
		showDigit(font, ones, heightInPages, widthInRows, page, column-widthInRows);
	}
	else if (number <= -10 && number > -100)
	{
		clearDigit(widthInRows, heightInPages, page, column-widthInRows*3);
		clearArea(page+heightInPages/2, column-widthInRows*3-NEGATIVE_SIGN_WIDTH, page+heightInPages/2, column-widthInRows*3);
		tens = ((-number)%100)/10;
		ones = (-number)%10;
		showIcon(negativeSign, NEGATIVE_SIGN_WIDTH, 1, page+heightInPages/2, column-widthInRows*2-NEGATIVE_SIGN_WIDTH);
		showDigit(font, tens, heightInPages, widthInRows, page, column-widthInRows*2);
		showDigit(font, ones, heightInPages, widthInRows, page, column-widthInRows);
	}
	else if (number <= -100)
	{
		showIcon(negativeSign, NEGATIVE_SIGN_WIDTH, 1, page+heightInPages/2, column-widthInRows*3-NEGATIVE_SIGN_WIDTH);
	    hundreds = (-number)/100;
		tens = ((-number)%100)/10;
		ones = (-number)%10;
		showDigit(font, hundreds, heightInPages, widthInRows, page, column-widthInRows*3);
		showDigit(font, tens, heightInPages, widthInRows, page, column-widthInRows*2);
		showDigit(font, ones, heightInPages, widthInRows, page, column-widthInRows);
	}
}

